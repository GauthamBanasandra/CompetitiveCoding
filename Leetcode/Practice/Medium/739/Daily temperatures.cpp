vector<int> Solution::dailyTemperatures(vector<int> &t) {
  vector<int> next_temperature(t.size());
  map<int, vector<size_t>> recent_high;

  for (size_t i = 0; i < t.size(); ++i) {
    auto find_it = upper_bound(
        recent_high.rbegin(), recent_high.rend(), t[i],
        [](const int temp, const pair<int, vector<size_t>> &recent) -> bool {
          return temp > recent.first;
        });
    for (auto it = find_it; it != recent_high.rend(); ++it) {
      for (const auto &index : it->second) {
        next_temperature[index] = i - index;
      }
    }
    if (find_it != recent_high.rend()) {
      recent_high.erase(recent_high.begin(), find_it.base());
    }
    recent_high[t[i]].emplace_back(i);
  }
  return next_temperature;
}
